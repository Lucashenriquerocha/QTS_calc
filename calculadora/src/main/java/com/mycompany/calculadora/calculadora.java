public class calculadora {
    //Soma
    public int somar(int a, int b){
        return a + b;
    }
    //Subtração
    public int subtracao(int a, int b){
        return a - b;
    }
    //Multiplicação
    public int multiplicacao(int a, int b){
        return a * b;
    }
    //Divisão
    public double divisao (double a, double b){
        return a / b;
    }
    
}
