package com.mycompany.calculadora;

/**
 *
 * @author lucas
 * @version 1.2
 * @since Release 1.2 da aplicação
 */
public class calculadora {
    /**
     * 
     * @param a , primeiro valor utilizado para a soma
     * @param b , segundo valor utilizado para a soma
     * @return , resultado
     */
    //Soma
    public int somar(int a, int b){
        return a + b;
    }
     /**
     * 
     * @param a , primeiro valor utilizado para a subtração
     * @param b , segundo valor utilizado para a subtração
     * @return , resultado
     */
    //Subtração
    public int subtracao(int a, int b){
        return a - b;
    }
     /**
     * 
     * @param a , primeiro valor utilizado para a multiplicação
     * @param b , segundo valor utilizado para a multiplicação
     * @return , resultado
     */
    //Multiplicação
    public int multiplicacao(int a, int b){
        return a * b;
    }
     /**
     * 
     * @param a , primeiro valor utilizado para a divisão
     * @param b , segundo valor utilizado para a divisão
     * @return , resultado
     */
    //Divisão
    public double divisao (double a, double b){
        return a / b;
    }
    
}
